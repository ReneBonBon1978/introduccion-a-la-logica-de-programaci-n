# Calificaciones


# ENTRADAS DE DATOS
# Declarar o crear las variables
calificación_1 = float(input("Ingresa la calificacion uno: "))
calificación_2 = float(input("Ingresa la calificacion dos: "))
calificación_3 = float(input("Ingresa la calificacion tres: "))

# Procesos (Cálculos Matemáticos y/o Lógicos)
promedio = (calificación_1 + calificación_2 + calificación_3) / 3

# Salida de datos
print(f"La calificacion promedio es = {round(promedio,2)}")
